    @Test
    @Transactional
    void getAllPaymentsByTransacIsEqualToSomething() throws Exception {
        Transac transac;
        if (TestUtil.findAll(em, Transac.class).isEmpty()) {
            paymentRepository.saveAndFlush(payment);
            transac = TransacResourceIT.createEntity(em);
        } else {
            transac = TestUtil.findAll(em, Transac.class).get(0);
        }
        em.persist(transac);
        em.flush();
        payment.setTransac(transac);
        paymentRepository.saveAndFlush(payment);
        Long transacId = transac.getId();

        // Get all the paymentList where transac equals to transacId
        defaultPaymentShouldBeFound("transacId.equals=" + transacId);

        // Get all the paymentList where transac equals to (transacId + 1)
        defaultPaymentShouldNotBeFound("transacId.equals=" + (transacId + 1));
    }

